public class RouteDestinations {
    private int id;
    private int number;
    private int stopDistanceId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getStopDistanceId() {
        return stopDistanceId;
    }

    public void setStopDistanceId(int stopDistanceId) {
        this.stopDistanceId = stopDistanceId;
    }
    
}
